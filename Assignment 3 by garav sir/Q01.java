//1) Program to print characters of all the ASCII values from 91 to 96
import java.util.Scanner;
 class Q01{
    public static void main(String args[]  ){
        
        System.out.println("ASCII values from 91 to 96 with characters:");
        
        for (int i = 91; i <= 96; i++) {
            System.out.println(i + " = " + (char)i);//char is used for char print bcz 91-96 there is value 
        }
    }
}

    





